#!/bin/bash

# delete any old node_modules directories
find . -name 'node_modules' -type d -exec rm -r "{}" \;

# find everything in directory 'apps'
dirlist=$(find apps -mindepth 1 -maxdepth 1 -type d)

for dir in $dirlist
do
  pushd $dir
  echo $dir
  echo "***** npm install *****"
  npm install
  echo "***** npm run build *****"
  npm run build
  echo "***** done for $dir *****"
  popd
done
